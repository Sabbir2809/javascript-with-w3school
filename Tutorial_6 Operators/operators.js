// Operators
// Arithmetic Operators(+, -, *, **, /, %, ++, --)
let x = 10;
let y = 2;
x--;
y++;
// precedence: * > / > + > -
let add = x + y;
let subtraction = x - y;
let multiplication = x * y;
let exponentiation = x ** y; // Math.pow(x,y)
let division = x / y;
let modulus = x % y;


// Assignment Operators (=, +=, -=, *=, /=, %=)
let a = 10;
a += 5; // a = a + 5;

let b = 10;
b -= 5; // b = b - 5;

let c = 10;
c *= 5; // c = c * 5;

let d = 10;
d /= 5; // d = d / 5;

let e = 10;
e %= 5; // e = e % 5;


